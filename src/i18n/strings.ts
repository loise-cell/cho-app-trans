const TABLES: Record<UiLanguageCode, Dictionary> = {
  "zh-TW": zhTW,
  "zh-CN": zhCN,
  en,
  ja,
  ko,
  es,
  fr,
  de,
  pt,
  vi,
  th,
  id
};

export type TranslateVars = Record<string, string | number>;

export function t(lang: UiLanguageCode, key: StringKey, vars?: TranslateVars): string {
  const table = TABLES[lang] ?? TABLES.en;
  let text = table[key] ?? TABLES.en[key] ?? key;
  if (vars) {
    for (const [name, value] of Object.entries(vars)) {
      text = text.replace(new RegExp(`\\{${name}\\}`, "g"), String(value));
    }
  }
  return text;
}
